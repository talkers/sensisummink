/*
 * colours.h
 * the list of ansi foreground and background colours.
 */

/* foreground */
#define BLK "\033[30m"
#define RED "\033[31m"
#define GRN "\033[32m"
#define YEL "\033[33m"
#define BLU "\033[34m"
#define MAG "\033[35m"
#define CYN "\033[36m"
#define WHT "\033[m" /* just happens to also be normal */

/* bright foreground */
#define HBLK "\033[1;30m"
#define HRED "\033[1;31m"
#define HGRN "\033[1;32m"
#define HYEL "\033[1;33m"
#define HBLU "\033[1;34m"
#define HMAG "\033[1;35m"
#define HCYN "\033[1;36m"
#define HWHT "\033[1;37m"

#define NOR "\033[m" /* normal text */

